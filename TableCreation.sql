CREATE TABLE SALES(
 ORDER_ID NUMBER,
 PRODUCT_ID NUMBER,
 CUSTOMER_ID NUMBER,
 EMPLOYEE_ID NUMBER,
 TOTAL NUMBER,
 QUANTITY NUMBER,
 SALES NUMBER
);

CREATE TABLE PRODUCT(
 PRODUCT_ID NUMBER,
 PRODUCT_NAME VARCHAR2(50),
 PRODUCT_CATEGORY_ID NUMBER
);

CREATE TABLE PRODUCT_CATEGORY(
 PRODUCT_CATEGORY_ID NUMBER,
 NAME VARCHAR2(50),
 DESCRIPTION VARCHAR2(500),
 UNIT_PRICE NUMBER
);



